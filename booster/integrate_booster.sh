            # Update ticket prefix
            sed -i.bak "s/ticketIdPrefix: '[^']*'/ticketIdPrefix: '$INTERACTIVE_TICKET_PREFIX'/g" "$config_file"

            # Update requireTickets flag
            sed -i.bak "s/requireTickets: false/requireTickets: true/g" "$config_file"


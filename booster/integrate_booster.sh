function confirm_action() {
    local message="$1"
    local default="${2:-n}"
    local response

    if [ "$default" = "y" ]; then
        prompt "$message [Y/n]: "
    else
        prompt "$message [y/N]: "
    fi

    read -r response
    response=${response:-$default}

    if [[ "$response" =~ ^[Yy]$ ]]; then
        return 0
    else
        return 1
    fi
}

